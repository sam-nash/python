# Arithmetic operations follow the order below

#  two multiplication symbols makes a power relationship
exponent = 2 ** 3  # 2 to the power of 3
print(exponent)  # result = 8

# modulo (%) operator, which returns the integer remainder of the division
remainder = 14 % 6
print(remainder)  # result = 2

# // operator gives the integer quotient of the division
integerDivision = 14//6
print(integerDivision)  # result = 2

# / operator gives the float quotient of the division
division = 14/6
print(division)  # result = 2.3333333333333335

# * operator gives the multiplication result of two operands
multiplication = 12*3
print(multiplication)  # result = 36

# - Subtraction
subtraction = 5-6
print(subtraction)  # result = -1

# + Addition
addition = 1+1
print(addition)  # result = 2

print((7 - 3) * ((3 + 5) / (6 - 2)))  # result = 8.0

# Augmented Assignment
str = 'Greetings '
str += 'People'
print(str)  # prints 'Greeting People'

num = 2
num += 3
print(num)  # result of 2 + 3 = 5

num -= 2
print(num)  # result of 5 - 2 = 3

num *= 3
print(num)  # result of 3 * 3 = 9

num %= 5
print(num)  # num is now 9 % 5 = 4

num /= 2
print(num)  # num is now 4 / 2 = 2.0

num //= 2
print(num)  # num is now 2.0 // 2 = 1.0
